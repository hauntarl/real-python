print('BOOLEANS:')
# Objects of Boolean type may have one of two values, True or False
print(True, type(True))
print(False, type(False))

# Expressions in Python are often evaluated in Boolean context,
# meaning they are interpreted to represent truth or falsehood.
# A value that is true in Boolean context is sometimes said to be “truthy,”
# and one that is false in Boolean context is said to be “falsy.”

# The “truthiness” of an object of Boolean type is self-evident:
# Boolean objects that are equal to True are truthy (true),
# and those equal to False are falsy (false).
# But non-Boolean objects can be evaluated in Boolean context as well and
# determined to be true or false
